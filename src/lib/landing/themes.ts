// Helper to get theme by ID
export function getTheme(themeId: LandingThemeType): LandingTheme {
  return LANDING_THEMES[themeId] || LANDING_THEMES.general
}

// Helper to get theme color as CSS variable
export function getThemeStyles(themeId: LandingThemeType) {
  const theme = getTheme(themeId)
  return {
    '--theme-primary': theme.primaryColor,
    '--theme-secondary': theme.secondaryColor,
    '--theme-gradient': theme.gradient,
  } as React.CSSProperties
}

// Map insurance type to theme
